"""
Pedir al usuario dos numeros enteros y mostrar en pantalla el cociente y el resto.
"""
number_1 = input("Ingresa el primer numero: ")
number_2 = input("Ingresa el segundo numero: ")
number_1_int = int(number_1)
number_2_int = int(number_2) 
print(f"El cociente es: {number_1_int / number_2_int} y el resto es: {number_1_int % number_2_int}")
