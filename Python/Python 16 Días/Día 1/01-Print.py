# PRINT
"""
Declaracion que al ejecutarse muestra (Imprimre) por consola, los argumentos
que se espesifican entre parentesis.

- MOSTRAR TEXTO
Ingresamos entre comillas dobles o simple (yo prefiero las dobles, las simples
son para chars) la palabra o texto que queremos mostrar.

- MOSTRAR NUMERO
Ingresamos SIN comillas de ningun tipo el numero que queremos mostrar. Podemos tambien
realizar operaciones matematicas entre numeros sin usar comillas, print solo mostrara
el resultado de la operacion.

-COMILLAS
Si queremos mostrar comillas, la forma mas facil es escribiendo comillas simples dentro
de un string determinado por comillas dobles, o hacerlo a la inversa, escribir comillas
dobles dentro de un string determiado por comillas simples.
"""

# Mostrar texto
print("Hola Mundo")

# Mostrar Numero
print(100)
print(10+2)  # Operacion Matematica

# Comillas
print("Hola Mundo, te digo 'Hola'")  # Mostra Comillas simples
print('Hola Mundo, te digo "Hola"')  # Mostrar comillas dobles
