# OPERADORES DE COMPARACION
"""
Sirven para comparar dos o mas valores. El resultado de esta comparacion es un Booleano
- Si la comparacion es cierta devuelve True
- Si la comparacion es falsa devuelve False
"""

# Igual que
print(10 == 2)

# Mayor que
print(12 > 1)

# Mayor o igual
print(5 >= 5)

# Menor que
print(7 < 10)

# Menor o igual
print(10 <= 8)

# Diferente
print(10 != 1)

# Igualmente podemos comparar strings
print("Hola" == "Hola")
# Es sensible a mayusculas y minusculas
print("Hola" == "hola")
