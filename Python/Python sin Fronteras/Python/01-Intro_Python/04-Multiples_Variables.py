# MULTIPLES VARAIBLES

"""Es posible declarar varias variables a la vez, en una sola linea.
Aqunque esto no es comun, en algunos casos puede ayudar a ahorrar espacio."""


# Ejemplos:
variable1, variable2, variable3 = "Valor 1", "Valor 2", "Valor 3"
print(variable1, variable2, variable3)

# Podemos hacer que mutliples variables tengan el mismo valor
var1 = var2 = var3 = "Todas tendran el mismo valor"
print(var1, var2, var3)