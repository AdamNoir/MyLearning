# LOS COMENTARIOS EN PYTHON

"""Los Comentarios son mensajes que nosotros mismos nos dejamos para despues, o que dejamos
 a otro desarrolladores, para tener una idea mas clara sobre en que estabamos trabajando, o como es
 que funciona alguna seccion del codigo.

 Estos comentarios NO SON tomados en cuenta a la hora de ejeuctar el programa.

 Existen varios tipos de comentarios:
    - De una sola linea, usando el simbolo '#'
    - De multiples Lineas usando triole comillas al principio y al final.

 Un comentario puede ir arriba o debajo del codigo sin problemas.
 Pero un comentario siempre debe de ir a la derecha de cualquier linea que vaya a ejecutarse, pues
 de lo contrario no sera ejecutada.

 NOTA: Un comentario # debe tener un espacio.
 Si el comentario esta a la derecha del codigo, debe haber dos espacios y despues el comentario."""

# Ejemplo:
# print("Hola Mundo")
print("Hola Mundo")  # Este mensaje sera ignorado pero no el Hola Mundo
